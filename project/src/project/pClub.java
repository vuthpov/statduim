    public pClub(JDialog dialog,MyModel modelClub,int selectedRowIndex){
        initComponents();
        
        this.dialog=dialog;
        this.modelClub=modelClub;
        this.selectedRowIndex=selectedRowIndex;
        originImagePath=pbImage.getIconAbsolutePath();
        
        prepareForm();
        
        String club=modelClub.getValueAt(selectedRowIndex, 1)+"";
        String shortName=modelClub.getValueAt(selectedRowIndex,2)+"";
        String league=((IdNameAndImage)modelClub.getValueAt(selectedRowIndex,3)).getName();
        String imageUrl=(modelClub.getValueAt(selectedRowIndex,4))+"";
        
        
        txtClub.setOriginValueAndSetTxt(club);
        txtShortName.setOriginValueAndSetTxt(shortName);
        
        for(int i=0;i<cbLeague.getItemCount();i++){
            
            
            
            
            if((cbLeague.getItemAt(i)+"").equals(league)){
                cbLeague.setSelectedIndex(-1);
                cbLeague.setSelectedIndex(i);
                break;
            }
        }
        
        File f=new File(imageUrl);
        if(f.exists()){
            ImageIcon icon=new ImageIcon(f.getAbsolutePath());
            pbImage.setIcon(icon);
        }
        
        
        
        
    }
    
    
