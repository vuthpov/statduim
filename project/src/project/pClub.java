    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        
        
        if(cbLeague.getSelectedIndex()==-1){
            cbLeague.setBorder(new LineBorder(new Color(203,25,21)));
            return;
        }else{
            cbLeague.setBorder(originCbBorder);
        }
        
        String club=txtClub.getThisText();
        
        IdNameAndImage league=new IdNameAndImage(((IdNameNoImage)cbLeague.getSelectedItem()).getId(),((IdNameNoImage)cbLeague.getSelectedItem()).getName(),((IdNameNoImage)cbLeague.getSelectedItem()).getImage());
        
        
        
        
        
        
        String shortName=txtShortName.getThisText();
        String imageUrl=pbImage.getIconAbsolutePath();
       
        if(selectedRowIndex>=0){
            
            String clubId=modelClub.getValueAt(selectedRowIndex, modelClub.getColumnCount()-1)+"";
            
           
           
            String[] data={club,shortName,league.getId(),imageUrl};
            
            if(Club.update(clubId,data)){
                
                
                
                Object[] obj={club,shortName,league,imageUrl};
                
                for(int i=1;i<=obj.length;i++){
                    modelClub.setValueAt(obj[i-1], selectedRowIndex, i);
                }
                
                
                
                
                
                JOptionPane.showMessageDialog(this, "Updated successful", "",JOptionPane.INFORMATION_MESSAGE);
                
                
            }
            
            
        }else{
            
            
            if(Club.insert(club,shortName,league.getId(),imageUrl)){

                
                String clubId=dataCon.one_cell_value("select max(clubId) from club") +"";
                
                modelClub.addRow(modelClub.getRowCount()+1,club,shortName,league,imageUrl,clubId);
                
                
                txtClub.resetTextField();
                txtShortName.resetTextField();
                cbLeague.setSelectedIndex(-1);
                
                JOptionPane.showMessageDialog(this, "Inserted successful", "",JOptionPane.INFORMATION_MESSAGE);
                
                
                
            }
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

    
     
    

