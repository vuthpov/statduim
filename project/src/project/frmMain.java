    public static int[] getCenterCordinate(Dimension FrameSize){
        int []centerCordinate=new int[2];
        
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
       
        
        centerCordinate[0]=(screenSize.width-FrameSize.width)/2;
        centerCordinate[1]=(screenSize.height-FrameSize.height)/2;
        
        return centerCordinate;
    }
    
    
    
    
    void centerForm(JComponent frm){
        try{
            
            
            Dimension desktopSize = dpContent.getSize();
            Dimension jInternalFrameSize = frm.getSize();
            
            int x=(desktopSize.width - jInternalFrameSize.width)/2;
            int y=(desktopSize.height- jInternalFrameSize.height)/2;
            
            
            if(frm.getX()!=x&&frm.getY()!=y)
                frm.setLocation(x,y);
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    
    void showForm(JInternalFrame frm){
       
        try {
            frm.setSelected(true);
        } catch (PropertyVetoException ex) {
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
        
        if(frm.isIcon()){
            try {

                frm.setIcon(false);

            } catch (PropertyVetoException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }
        }
        
    }
    
    public void getForm(JInternalFrame frm){
        showForm(frm);
            
        
        if(frm.getParent()==null)
            dpContent.add(frm);
        
        
        frm.setVisible(true);
                   
        centerForm(frm);
    }
    
